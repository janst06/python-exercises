"""Demonstrating type conversions in Python."""

print(int(5.5))  # 5
print(float(-1))  # -1.0
print(str(0.3))  # "0.3"
print(bool(0.0001))  # True
print(str("False"))  # "False"
print(int(False))  # 0
print(bool("0"))  # True
