"""Matrix Indexing Exercise."""

matrix = [[1, 0, 1],
          [0, 1, 0],
          [3, 2, 1],
          [1, 2, 3],
          [3, 0, 3]]

print(matrix[0][2])  # Output: 1
print(matrix[2][2])  # Output: 1
print(matrix[4][2])  # Output: 3
print(matrix[1][1])  # Output: 1
print(matrix[3][1])  # Output: 2
print(matrix[2][0])  # Output: 3
print(matrix[0])  # Output: [1, 0, 1]
print(matrix[2])  # Output: [3, 2, 1]
